#include "libft.h"
#include <unistd.h>

void	ft_putnbr_fd(int nb, int fd)
{
	long	num;

	num = nb;
	if (num < 0)
	{
		ft_putchar_fd('-', fd);
		num = -num;
	}
	if (num >= 10)
		ft_putnbr_fd(num / 10, fd);
	ft_putchar_fd((num % 10) + '0', fd);
}
